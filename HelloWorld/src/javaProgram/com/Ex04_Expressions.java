public class Ex04_Expressions {
    public static void main(String[] args) {

        //expression = operands & operators
        //operands = values, variables, numbers, quantities
        //operators = + - * / %

        double friends = 10;
        friends++;
        System.out.println(friends);

        friends = (double) friends / 3;
        System.out.println(friends);
    }
}
